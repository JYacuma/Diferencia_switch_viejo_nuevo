public class Main {
    public static void main(String[] args) {

        System.out.println("---DIAS DE LA SEMANA----");
        System.out.println(" ");

        int dia = 3;

        String nombreDelDia = switch (dia) {
            case 1 -> "Lunes";
            case 2 -> "Martes";
            case 3 -> "Miércoles";
            case 4 -> "Jueves";
            case 5 -> "Viernes";
            case 6 -> "Sábado";
            case 7 -> "Domingo";
            default -> "Día inválido";
        };

        System.out.println("El día es: " + nombreDelDia);


    }
}